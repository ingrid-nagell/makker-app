class Activity {
  final int id;
  final String userId;
  final String date;
  final String category;
  final String location;
  final String address;
  final String description;

  Activity({
    required this.id,
    required this.userId,
    required this.date,
    required this.category,
    required this.location,
    required this.address,
    required this.description,
  });
}
